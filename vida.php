                <div id="title">Deportes UPB</div>  
                <div id="box">
                	<h2>Objetivos Deportes: Objetivos Generales</h2>
                    <h2>Objetivos Generales:</h2>
                    <p id="carrera">I. Integrar la actividad Física y Deportiva dentro de las actividades de la Universidad.</p>
                    <p id="carrera">II.Ofrecer a través de la práctica deportiva, buena salud, la convivencia y el sano esparcimiento de toda la Comunidad Universitaria.</p>
                    <p id="carrera">III. Proporcionar los escenarios, campos deportivos y elementos necesarios para desarrollar las actividades deportivas que requiera la Universidad.</p>
                    <p id="carrera">IV. Brindar a estudiantes, profesores y funcionarios de la Universidad la posibilidad de desarrollar sus capacidades y habilidades físicas de acuerdo a sus intereses para mejorar su calidad de vida, así como su formación individual y social.</p>
                    <p id="carrera">V. Conformar con la Comunidad Universitaria, selecciones que representen competitivamente a la UPB, elevando su nivel competitivo permanentemente.</p>
                    <p id="carrera">VI. Establecer una política en Deportes que articule el modelo académico y la formación integral fortaleciendo valores como: la disciplina, la sana competencia, la honestidad y la capacidad de superación.</p>
                    <h2>Objetivos Específicos:</h2>
                    <h2>Deportivos:</h2>
                    <p id="carrera">1. Organizar actividades deportivas dentro de la Universidad que involucren el mayor número de estudiantes durante los semestres.</p>
                    <p id="carrera">2. Seleccionar y preparar los mejores equipos de la Universidad para que participen en representación local, nacional o internacional buscando los mejores resultados.</p>
                    <p id="carrera">3. Programar actividades de recreación y utilización del tiempo libre para la comunidad universitaria en forma permanente.</p>
                    <p id="carrera">4. Estructurar el Club Universidad Privada Boliviana que permita a nuestros deportistas participar local, nacional e internacionalmente en las competiciones del deporte asociado y universitario.</p>
                    <h2>Académicos:</h2>
                    <p id="carrera">5. Apoyar la realización y organización de eventos de las facultades para la difusión y realización de actividades deportivas.</p>
                    <p id="carrera">6. Capacitar permanentemente el grupo de entrenadores desarrollando para ello seminarios y curso internos.</p>
                    <h2>Administrativos:</h2>
                    <p id="carrera">7. Presentar al deporte como actividad alternativa preventiva de la drogadicción, alcoholismo y otros problemas sociales de la juventud.</p>
                    <p id="carrera">8. Establecer indicadores de Gestión de participación y resultados deportivos, controlando y evaluando los resultados anualmente.</p>	
