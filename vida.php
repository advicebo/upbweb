                	<h2>Que es?</h2>
                    <p id="carrera">La Universidad Privada Boliviana cuenta con el servicio de Orientación y Consejería, con el objetivo de orientar, asesorar y ofrecer ayuda psicológica a los estudiantes. Este servicio, se rige por los principios de confidencialidad, profesionalidad y es totalmente gratuito.</p>	
                	<h2>Que te Ofrece?</h2>
                    <p id="carrera">Servicio de Orientación Psicológica podrá ayudarte:</p>
                    <p id="carrera">• Al desarrollo óptimo de tus posibilidades, emocionales e intelectuales.</p>
                    <p id="carrera">• Al desarrollo satisfactorio de tu personalidad.</p>
                    <p id="carrera">• A la comprensión de sí mismo.</p>
                    <p id="carrera">• A la autodirección y el desarrollo de tu identidad personal.</p>   
                    <p id="carrera">Asimismo, específicamente te ofrece:</p>
                    <p id="carrera">- Evaluación y diagnóstico psicológico.</p>
                    <p id="carrera">- Aplicación de Pruebas de Orientación Vocacional</p>
                    <p id="carrera">- Apoyo en la mejora de estrategias y técnicas de estudio y aprendizaje.</p>
                    <p id="carrera">- Programas y acciones preventivas.</p>
                    <p id="carrera">El departamento de Orientación y Consejería, coordina el servicio de Mentorías y Tutorías.</p>
                    <p id="carrera">Todo esto se lleva a cabo a través de entrevistas personales y seguimiento individualizado, complementándose con diversas pruebas psicológicas y con la atención al estudiante durante todo el curso.</p>
