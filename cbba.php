                    <p id="carrera">El modelo académico aplicado en la UPB se denomina "Modelo Académico Basado en 
                    el Desempeño Profesional". Este modelo fue diseñado como respuesta a las necesidades de formación 
                    profesional que se necesitan en el país y que fueron identificadas por el empresariado boliviano.</p>

                    <p id="carrera">El modelo responde a un entorno altamente exigente de profesionales capacitados 
                    para atender rápidamente a los retos que el país enfrenta en estos tiempos de globalización.</p>

                    <p id="carrera">El Modelo Académico Basado en el Desempeño Profesional de la UPB es un modelo 
                    académico universitario que organiza el proceso educativo en tres dimensiones: Desarrollo de 
                    Competencias Profesionales, Formación para el Desempeño Profesional y el Proceso de Enseñanza - 
                    Aprendizaje.</p>

                    <p id="carrera">Desarrollo de Competencias Profesionales.- Esta dimensión del modelo se refiere a 
                    las capacidades que un individuo debe poseer, para desempeñar de manera competente un rol 
                    profesional específico. Ésta dimensión organiza las ciencias, las artes, las humanidades y la 
                    tecnología en cuatro áreas:</p> 
                    <p id="carrera">Competencias Conceptuales.- Son aquellas que se derivan de generalizaciones 
                    conceptuales de los contenidos científicos en cada una de las asignaturas universitarias.</p> 
                    <p id="carrera">Competencias Técnicas.- Son aquellas que demuestran el uso de procedimientos, 
                    métodos y técnicas específicas de una carrera profesional y a su vez desarrollan en el individuo 
                    un sistema de trabajo. 
                    <p id="carrera">Competencias Humanas.- Son aquellas que permiten al individuo interactuar en un 
                    contexto humano, empleando adecuadamente la comunicación y las relaciones interpersonales.</p> 
                    <p id="carrera">Competencias Competitivas.- Son aquellas que basadas en las actividades 
                    intelectuales de orden superior, desarrollan en el individuo características y atributos para 
                    plantear alternativas singulares y cursos creativos para resolver problemas en situaciones 
                    conflictivas y de alta competitividad profesional.</p>

                    <p id="carrera">Formación para el Desempeño Profesional.- Esta dimensión del modelo se refiere a los 
                    niveles de proficiencia, que el individuo adquiere como producto de su interacción con los contenidos 
                    académicos y los métodos de enseñanza universitarios. Es decir que el alumno gradualmente va demostrando 
                    el dominio y aprovechamiento del curriculum universitario y va desarrollando las competencias profesionales 
                    establecidas. Esta dimensión incluye los siguientes niveles de desempeño:</p> 
                    <p id="carrera">Familiaridad.- Este nivel se refiere al hecho de que el alumno conoce la 
                    información básica del fenómeno en estudio; y que además le permite compartir y argumentar 
                    de manera inteligente, un concepto, tópico, hecho o información relacionada con el evento en 
                    estudio.</p> 
                    <p id="carrera">Comprensión.- Este nivel demuestra el logro de una competencia profesional 
                    con mayor grado de dificultad. Esto implica, que el alumno ha demostrado la capacidad para 
                    comprender las relaciones cognoscitivas de las variables en estudio. Además, permite al alumno 
                    desarrollar la capacidad para enseñar a otra persona los conocimientos adquiridos.</p> 
                    <p id="carrera">Aplicación.- Este nivel de desempeño ilustra el hecho de que el alumno posee 
                    el nivel de proficiencia requerido para llevar a la práctica las competencias profesionales.</p> 
                    <p id="carrera">Habilidades Intelectuales de Orden Superior.- Este nivel refleja el dominio superlativo 
                    de una competencia profesional. En este nivel el individuo demuestra la capacidad para analizar los 
                    problemas, integrar variables, crear alternativas, resolver problemas, tomar decisiones, utilizar la 
                    creatividad, tomar riesgos y evaluar información, variables y hechos.</p>

                    <p id="carrera">El Proceso de Enseñanza - Aprendizaje.- Esta dimensión del modelo se refiere al diseño, 
                    producción, implementación y evaluación de la enseñanza universitaria. Cada competencia es desarrollada 
                    a un nivel establecido de proficiencia o desempeño profesional. Para lograr estos objetivos, los 
                    profesores deben organizar experiencias de aprendizaje que involucran al alumno en el control de 
                    sus propios recursos de aprendizaje y convierten al profesor en asesor y facilitador de la enseñanza.</p> 
                    <p id="carrera">Una de las características más destacadas de la forma en la que el proceso 
                    de enseñanza - aprendizaje está organizado en la UPB es su Sistema de Programación Académica 
                    modular. Este sistema de programación académica modular e intensivo es no tradicional y único
                    en Bolivia y en virtud al cual las materias son programadas de dos en dos y con una intensidad 
                    de dos horas diarias, lo que permite:</p>

                    <p id="carrera">- Concentrar la atención del alumno en dos áreas específicas de estudio.</p>

                    <p id="carrera">- Incrementar el rendimiento académico, logrando una distribución adecuada 
                    de la carga académica y un desarrollo completo de los temas planteados para cada sesión de 
                    clases.</p>

                    <p id="carrera">- Evitar el cruce de exámenes parciales o finales, permitiendo al alumno 
                    programar adecuadamente sus horarios de estudio.</p>

                    <p id="carrera">Nuestra programación modular e intensiva también permite que, en la 
                    de los casos, los alumnos que cursan semestres impares pasen clases únicamente en el horario 
                    de la mañana (07:45 a 12:00) a fin de que pueda utilizar la tarde para realizar sus trabajos 
                    y repasar sobre los temas vistos en clase y los alumnos que cursan semestres pares pasen clases 
                    en el horario de la tarde (14:30 a 18:45) contando con la mañana para desarrollar sus 
                    actividades.</p>

                    <p id="carrera">Nuestra gestión académica se encuentra dividida en dos semestres 
                    (el primero de Febrero a Junio, el segundo de Agosto a Diciembre). Así mismo nuestros 
                    alumnos cuentan con cursos especiales en los periodos intermedios de invierno y verano.</p>
