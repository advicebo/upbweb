                <div id="box">                  
                    <p id="carrera">El Ingeniero de Producción (IP) por su formación bilingüe (*), multidisciplinaria y su visión de conjunto tiene habilidades profesionales para:</p>
                        <p id="carrera">
                            • Combinar el manejo de los recursos naturales, humanos, financieros y tecnológicos en el entorno de los procesos<br/>
                            • Conciliar las operaciones de producción con las restricciones, requerimientos y cambios continuos operantes en nuestros días<br/>
                            • Diseñar, instalar, operar, controlar y optimizar los sistemas productivos o de transformación<br/>
                            • Diseñar e implementar sistemas de control de calidad<br/>
                            • Elaborar, evaluar e implementar proyectos de inversión agroindustriales<br/>
                            • Diseñar e implementar sistemas de gestión de medio ambiente<br/>          
                        </p>    
