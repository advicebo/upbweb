                    <p id="carrera">La carrera cuenta con un enfoque de Ingeniería de Procesos Industriales relacionada con el diseño, instalación, operación y optimización de los sistemas de transformación de las materias primas en productos y servicios. Estos sistemas involucran a recursos humanos, económicos, materiales y tecnología.</p>

<p id="carrera">La estructura de la carrera incluye un alto número de materias del área administrativa y gerencial que permiten además adquirir habilidades para la toma de decisiones y un conocimiento global de la organización.</p>
                </div>
