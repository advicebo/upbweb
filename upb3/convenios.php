                    	<h2>CEPB</h2>
                        <p id="carrera">La CEPB tiene el objetivo general de promover y participar activamente en el proceso de desarrollo económico y social de Bolivia, sustentando la filosofía de la libre empresa y la economía de mercado como bases esenciales de dicho proceso.</p>

<p id="carrera">Entre sus funciones se encuentra la de fomentar el perfeccionamiento de las técnicas de dirección, administración y productividad, así como estimular la capacitación y el adiestramiento en todos los niveles de la actividad empresarial privada. Asimismo, tiene la función de elaborar recomendaciones en materia socio económica, especialmente las referidas a los asuntos que confronte la actividad privada, a la política económica general y a todo tema de interés nacional.</p>

<p id="carrera">En este marco, desde enero de 1991, la Confederación de Empresarios Privados de Bolivia participa como patrocinador de la Universidad Privada Boliviana inicialmente representada por su Presidente Ing. Fernando Illanes de la Riva. Actualmente, la CEPB participa en el Directorio de la UPB a través su Presidente Ing. Ramiro Mustafá Schnor, como Director Ex-oficio.</p>

<p id="carrera">La CEPB es también patrocinadora de la Becas Confederación a través de las cuales, estudiantes universitarios de universidades públicas y privadas, con alto rendimiento y bajos recursos económicos tienen la oportunidad acceder a una educación universitaria de alta calidad en la UPB, en carreras que son de interés para el desarrollo económico y social del país.</p>

<p id="carrera">Sitio web oficial <a href="http://www.cepb.org.bo/">Confederaci&oacute;n de Empresarios Privados de Bolivia</a></p>
