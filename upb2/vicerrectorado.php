                    <p>La Misión de la Universidad Privada Boliviana destaca claramente la investigación científica y aplicada como actividad fundamental de su quehacer cotidiano, rescatando el papel de locus del conocimiento de la Universidad.</p>

<p>El Decanato de Investigación, dependiente del Vicerrectorado Académico, es la instancia encargada de implementar las políticas institucionales de investigación derivadas de esta Misión, las mismas que se traducen en los siguientes objetivos del Decanato:

<p>Desarrollar la investigación como función esencial de la Universidad, con fines académicos y de interacción con el entorno, estrechamente vinculada con las funciones de enseñanza, aprendizaje y extensión y en procura de llegar a construir una competencia distintiva institucional en este campo.</p>

<p>Destacar las actividades de investigación por su calidad, su pertinencia tanto interna como externa, su continuidad en el mediano y largo plazo y su sostenibilidad científica y económica.</p>

<p>Enfocar fuertemente la investigación en las más altas prioridades externas compatibles con los programas de formación existentes o a ofrecerse en el futuro</p>

<p>Introducir apropiadamente a la investigación en el ámbito de los diseños curriculares, tanto en pregrado como en postgrado, como un medio imprescindible para alcanzar calidad en los procesos de aprendizaje, en los estudiantes graduados y en los servicios a la comunidad.</p>

<p>Coordinar / Integrar apropiadamente los procesos de enseñanza de pregrado y postgrado, la investigación y la prestación de servicios especializados al interior de la estructura universitaria.</p>

<p>Establecer un Sistema de Gestión del Desempeño de la Investigación, para medir, evaluar, gestionar y mejorar los procesos de investigación en todas las instancias académicas de la Universidad.</p>

<p>Establecer una capacidad universitaria de contraparte para proyectos de cooperación internacional.<div id="descarga"><a target="_blank" href="reglamentos/i-d.php">Reglamento de Investigación</a></div></p> 
              
